"""
Playwright CLI Tools
===================

Tools for interacting with Playwright CLI to run Playwright Test Agents (planner, generator, healer).
"""

import os
import subprocess
from pathlib import Path
from typing import Optional

from agno.tools import tool

# Configuration
AUTOMATION_DIR = Path(__file__).parent.parent.parent / "automation"
TECHNICAL_TESTS_DIR = AUTOMATION_DIR / "technical-tests"


@tool
def init_playwright_agents() -> str:
    """Initialize Playwright Test Agents in the automation directory.

    This runs `npx playwright init-agents --loop=vscode` to generate
    the agent definitions for planner, generator, and healer.

    Returns:
        str: Success message or error details
    """
    try:
        result = subprocess.run(
            ["npx", "playwright", "init-agents", "--loop=vscode"],
            cwd=AUTOMATION_DIR,
            capture_output=True,
            text=True,
            timeout=120,
        )
        
        if result.returncode == 0:
            return "Playwright Test Agents initialized successfully. Agent definitions generated in .github/agents/"
        else:
            return f"Error initializing Playwright Test Agents: {result.stderr}"
    except subprocess.TimeoutExpired:
        return "Error: Playwright init-agents command timed out"
    except Exception as e:
        return f"Error initializing Playwright Test Agents: {str(e)}"



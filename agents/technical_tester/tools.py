@tool
def list_generated_tests() -> str:
    """List all generated Playwright tests in the technical-tests directory.

    Returns:
        str: List of test files or message if none exist
    """
    try:
        tests_dir = TECHNICAL_TESTS_DIR / "tests"
        
        if not tests_dir.exists():
            return "No tests directory found. Run generator first."
        
        test_files = list(tests_dir.glob("*.spec.ts"))
        
        if not test_files:
            return "No test files found in technical-tests/tests/"
        
        result = "Generated tests:\n"
        for test_file in sorted(test_files):
            result += f"  - {test_file.name}\n"
        
        return result
    except Exception as e:
        return f"Error listing tests: {str(e)}"


@tool
def run_tests(test_file: Optional[str] = None) -> str:
    """Run generated Playwright tests.

    Args:
        test_file (str): Optional specific test file to run. If not provided, runs all tests.

    Returns:
        str: Test execution results
    """
    try:
        tests_dir = TECHNICAL_TESTS_DIR / "tests"
        
        if test_file:
            test_path = tests_dir / test_file
            if not test_path.exists():
                return f"Test file not found: {test_file}"
            cmd = ["npx", "playwright", "test", str(test_path)]
        else:
            if not tests_dir.exists():
                return "No tests directory found. Run generator first."
            cmd = ["npx", "playwright", "test", str(tests_dir)]
        
        result = subprocess.run(
            cmd,
            cwd=AUTOMATION_DIR,
            capture_output=True,
            text=True,
            timeout=300,
        )
        
        if result.returncode == 0:
            return f"Tests passed:\n{result.stdout}"
        else:
            return f"Tests failed:\n{result.stderr}"
    except subprocess.TimeoutExpired:
        return "Error: Test execution timed out"
    except Exception as e:
        return f"Error running tests: {str(e)}"


